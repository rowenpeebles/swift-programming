// Comparison Operators

var a = 5, b = 2

// 'a' is equal to 'b'
print(a == b)

// 'a' is not equal to 'b'
print(a != b)

// 'a' is greater than 'b'
print(a > b)

// 'a' is less than 'b'
print(a < b)

// 'a' is greater than or equal to 'b'
print(a >= b)

// 'a' is less than or equal to 'b'
print(a <= b)
