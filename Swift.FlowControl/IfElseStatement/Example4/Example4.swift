// Nested 'if...else' statement

var number = 5

// Outer 'if' statement
if (number >= 0) {
    
    // Inner 'if' statement
    if (number == 0) {
        print("The number is 0.")
    }

    // Inner 'else' statement
    else {
        print("The number is positive.")
    }

}

// Outer 'else' statement 
else {
    print("The number is negative.")
} 
