// Swift Data Types

// Character
var letter: Character = "s"
print(letter)

// String
var language: String = "swift"
print(language)

// Integer
var number: Int = 3
print(number)

// Boolean
let passCheck: Bool = true
print(passCheck)
let failCheck: Bool = false
print(failCheck)

// Float
let piValue: Float = 3.14
print(piValue)

// Double
let latitude: Double = 27.7007697012432
print(latitude)
